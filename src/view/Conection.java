    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Client client = getParam();

        int i = clientService.seConnecter(client);
        if (i > 0) {
            Session.updateAttribute(client, "conectedClient");
            JOptionPane.showMessageDialog(null, "CONNECTION AVEC SUCCES BIENVENU : " + client.getLogin(), "INFO", JOptionPane.INFORMATION_MESSAGE);
            new Acceuil().setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "CONNECTION ECHEC ", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

